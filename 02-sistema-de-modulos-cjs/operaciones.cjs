const suma = (numA, numB) => {
    return numA + numB;
}

const resta = (numA, numB) => {
    return numA - numB;
}

const multiplicacion = (numA, numB) => {
    return numA * numB;
}

module.exports={
    suma,
    resta,
    multiplicacion,
}
